package sec2.exam02;

public class Printer {
	//작성 위치
	
	int number;
	boolean yesOrno;
	double num_b;
	String name;
	
	
	void println (int number) {
		System.out.println("number:"+number);
	}	
	void println (boolean yesOrno) {
		System.out.println("number:"+yesOrno);
	}	
	
	void println (double num_b) {
		System.out.println("number:"+num_b);
	}	
	
	
	void println (String name) {
		System.out.println("number:"+name);
	}	
	

}
